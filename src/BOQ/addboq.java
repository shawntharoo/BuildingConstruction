void tableload_search(String search){
    
        String bid=jLabel30.getText();
        
    
    DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
    dtm.setRowCount(0);
        try {
           ResultSet rs= db.getconnection().createStatement().executeQuery("select * from boq_mat_all where id Like'"+search+"%' and boq_id='"+bid+"'");
              
           while(rs.next()){  
                
                Vector v = new Vector();

                v.add(rs.getString("id"));
            v.add(rs.getString("level"));
            v.add(rs.getString("sec"));
            v.add(rs.getString("re_type"));
            v.add(rs.getString("mat"));
            v.add(rs.getString("app"));
            v.add(rs.getString("quntity"));
            v.add(rs.getString("unit"));
            v.add(rs.getString("rate"));
            v.add(rs.getString("markup"));
            v.add(rs.getString("total"));
          
            
                dtm.addRow(v);
              
              }
        
        }catch(Exception e){
            e.printStackTrace();
        }
}

