public void caltotal(){
        try {
            String id=jLabel30.getText();
            
            Statement st= db.getconnection().createStatement();
            ResultSet rs=st.executeQuery("SELECT SUM(total) FROM boq_mat_all where boq_id='"+id+"'");
           
            if(rs.next()){ 
            
            jLabel22.setText(rs.getString("sum(total)"));
            }
            
            
        } catch (Exception e) {
            e.printStackTrace();
        }
}

