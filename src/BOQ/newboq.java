public void incnum(){


        try {
            Statement st= db.getconnection().createStatement();
            
            ResultSet sp =st.executeQuery("SELECT COUNT(*) AS RowCnt FROM boq");
            
            while(sp.next()){
                
                String nm=sp.getString(1);
                int x =Integer.parseInt(nm);
                
                if(x==0){
                    jLabel12.setText("1001");
                }
                else{
                
                    ResultSet ss =st.executeQuery("SELECT MAX(BOQ_id) FROM boq");
            
                        while(ss.next())
                        {
                            String no=ss.getString(1);

                            int nu=Integer.parseInt(no);
                            nu=++nu;
                            String var = String.format("%04d", nu);

                            jLabel12.setText(var);
                        }
                
                }
                
                break;
            }
                      
        } catch (Exception e) {
            e.printStackTrace();
        }


}

