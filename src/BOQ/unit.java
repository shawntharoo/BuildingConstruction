public  void tableload(){
           
        
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
         try {
           ResultSet rs= db.getconnection().createStatement().executeQuery("select * from unit");
           
           while(rs.next()){
                 Vector v = new Vector();
         
            v.add(rs.getString("unit_id"));
            v.add(rs.getString("unit_name"));
            
                                 
             dtm.addRow(v);
             
             
      
           }
    }
         catch(Exception e){
         e.printStackTrace();
         
         }

    }
public void selectrow(){
 
 try {
            int r =jTable1.getSelectedRow();
            String a=jTable1.getValueAt(r, 0).toString();
            
           Statement st= db.getconnection().createStatement();
           ResultSet ss =st.executeQuery("SELECT * FROM unit WHERE unit_id='"+a+"' ");
               
                
            while(ss.next())
            {
                String no=ss.getString(1);
                String na=ss.getString(2);
                
                
                jLabel3.setText(no);
                jTextField1.setText(na);
                
            }
                
        } catch (Exception e) {
            e.printStackTrace();
        }
 
 }

