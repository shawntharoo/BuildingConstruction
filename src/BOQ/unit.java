public void incnum(){


        try {
            Statement st= db.getconnection().createStatement();
            
            ResultSet sp =st.executeQuery("SELECT COUNT(*) AS RowCnt FROM unit");
            
            while(sp.next()){
                String nm=sp.getString(1);
                int x =Integer.parseInt(nm);
                
                
                if(x==0){
                jLabel3.setText("001");
                }
                else{
                
                ResultSet ss =st.executeQuery("SELECT MAX(unit_id) FROM unit");
            
            while(ss.next())
            {
                String no=ss.getString(1);
                
                int nu=Integer.parseInt(no);
                nu=++nu;
                String var = String.format("%03d", nu);

                jLabel3.setText(var);
            }
                
                }
                
                break;
            }
            
            
            
            
        } catch (Exception e) {
            e.printStackTrace();
        }


}
