    private void loadvoucher() {
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        try {
            ResultSet rs= db.statement().executeQuery("select * from supplier_voucher");
          
         while(rs.next()){
             Vector v = new Vector();
             v.add(rs.getString("id"));
             v.add(rs.getString("dates"));
             v.add(rs.getString("amount"));
             v.add(rs.getString("payment_method"));
             
             dtm.addRow(v);
         }
         
           
        } catch (Exception e) {
        }
    }
