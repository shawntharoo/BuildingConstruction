    private void loadimage(String img_name) {
        try {
            ResultSet rs= db.getconnection().createStatement().executeQuery("select img from spc_report where name='"+img_name+"' and id='"+conall.num+"' ");
            if(rs.next()){
                ImageIcon img = new ImageIcon(rs.getString("img"));
                jLabel1.setIcon(img);
            }
            
        } catch (Exception e) {
        }
    }
