    private void cost_history() {
        try {
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            ResultSet rs= db.getconnection().createStatement().executeQuery("select * from project_level_payment where projectid="+conall.num+"");
            while(rs.next()){
                Vector v = new Vector();
                String cost= rs.getString("cost");
                String project= rs.getString("date");
                
                v.add(project);
                v.add(cost);
                dtm.addRow(v);
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
